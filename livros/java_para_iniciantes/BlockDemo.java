package livros.java_para_iniciantes;

public class BlockDemo {
    public static void main(String[] args) {
        double i, j, d;

        i = 5;
        j = 10;

        if (i != 0) {
            System.out.println("i does not equals zero.");
            d = j / i;
            System.out.println("j / i is " + d);
        }
    }
}
